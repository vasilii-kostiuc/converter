	public function findRate(int $fromCurrId, int $toCurrId) {
		return $this->createQueryBuilder('cr')
			->andWhere('cr.fromCurrency = :fromCurrency')
			->andWhere('cr.toCurrency = :toCurrency')
			->setParameter('fromCurrency', $fromCurrId)
			->setParameter('toCurrency', $toCurrId)
			->setMaxResults(1)
			->orderBy('cr.date', 'DESC')
			->getQuery()
			->getOneOrNullResult();
	}
